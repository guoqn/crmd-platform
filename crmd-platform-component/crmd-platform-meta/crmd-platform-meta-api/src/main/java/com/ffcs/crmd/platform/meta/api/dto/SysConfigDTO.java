package com.ffcs.crmd.platform.meta.api.dto;

import java.io.Serializable;

import com.ffcs.crmd.platform.core.ddd.api.dto.impl.DomBaseDTO;


public class SysConfigDTO extends DomBaseDTO implements Serializable{

	/**
	 * 
	 */
	private static final long serialVersionUID = -5464680379446463163L;
	private Long confId;
	private String name;
	private String nodeValue;
	private Long parentId;
	private String code;
	private String sysConfDesc;
	private String parentName;
	public Long getConfId() {
		return confId;
	}
	public void setConfId(Long confId) {
		this.confId = confId;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getNodeValue() {
		return nodeValue;
	}
	public void setNodeValue(String nodeValue) {
		this.nodeValue = nodeValue;
	}
	public Long getParentId() {
		return parentId;
	}
	public void setParentId(Long parentId) {
		this.parentId = parentId;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getSysConfDesc() {
		return sysConfDesc;
	}
	public void setSysConfDesc(String sysConfDesc) {
		this.sysConfDesc = sysConfDesc;
	}
	public String getParentName() {
		return parentName;
	}
	public void setParentName(String parentName) {
		this.parentName = parentName;
	}
	

}
