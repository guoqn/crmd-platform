package com.ffcs.crmd.platform.pub.interfaces;

import com.ffcs.crmd.platform.pub.interfaces.log.IInterfLog;

/**
 * Created by linzhiqiang on 16/5/1.
 */
public class TestInterfLog implements IInterfLog {

    private String name;

    public TestInterfLog(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "TestInterfLog{" +
            "name='" + name + '\'' +
            '}';
    }
}
