package com.ffcs.crmd.platform.pub.pool.exception;

import com.ctg.itrdc.platform.common.exception.RtManagerException;

import java.util.Map;

/**
 * Created by linzq on 2016/1/18.
 */
public class CrmPoolException extends RtManagerException {
    public CrmPoolException() {
    }

    public CrmPoolException(Throwable throwable) {
        super(throwable);
    }

    public CrmPoolException(String message) {
        super(message);
    }

    public CrmPoolException(String message, Class clazz, String method, String code) {
        super(message, clazz, method, code);
    }

    public CrmPoolException(String message, Class clazz, String method, String code, Map dataMap) {
        super(message, clazz, method, code, dataMap);
    }

    public CrmPoolException(String message, Class clazz, String method, String code,
        Object... params) {
        super(message, clazz, method, code, params);
    }

    public CrmPoolException(String message, Throwable throwable) {
        super(message, throwable);
    }

    public CrmPoolException(String message, Throwable throwable, String code) {
        super(message, throwable, code);
    }
}
