/**
 * 参数规则
 * 
 * @author LAIYONGMIN-PC
 *
 */
public class ArgRule {

	/**
	 * TYPE_MUST.
	 */
	public static final String TYPE_MUST = "must";

	/**
	 * TYPE_NEED.
	 */
	public static final String TYPE_NEED = "need";

	/**
	 * arg.
	 */

	private String[] arg;

	/**
	 * type.
	 */

	private String type;

	public String[] getArg() {
		return arg;
	}

	public void setArg(String[] arg) {
		this.arg = arg;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

}
